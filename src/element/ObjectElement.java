package element;

public class ObjectElement {
    int x  ;
    int y ;
    int Width ;
    int height ;
    int effet ;
    public int getX() {
        return x;
    }
    public void setX(int x) {
        this.x = x;
    }
    public int getY() {
        return y;
    }
    public void setY(int y) {
        this.y = y;
    }
    public int getWidth() {
        return Width;
    }
    public void setWidth(int width) {
        Width = width;
    }
    public int getHeight() {
        return height;
    }
    public void setHeight(int height) {
        this.height = height;
    }
    public int getEffet() {
        return effet;
    }
    public void setEffet(int effet) {
        this.effet = effet;
    }
}
